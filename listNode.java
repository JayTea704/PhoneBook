//Creates a single node on linked list
public class listNode {

    public listNode next;

    public String name;
    public String phone;
    public String address;
    public String city;

    public listNode(String name, String phone, String address, String city) {

        this.name = name;
        this.phone = phone;
        this.address = address;
        this.city = city;
    }
}//end of listNode class
